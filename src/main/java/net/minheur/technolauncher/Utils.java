package net.minheur.technolauncher;

import javax.swing.*;
import java.awt.*;

public class Utils {
    public static void buttonStyle(JButton pbutton) {
        pbutton.setBackground(new Color(50, 50, 50));
        pbutton.setForeground(Color.WHITE);
        pbutton.setFocusPainted(false);
    }
}
